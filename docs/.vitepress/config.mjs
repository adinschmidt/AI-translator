function createLocaleEntry(key, config) {
    return {
        label: config.label,
        lang: config.lang,
        ...(config.link ? { link: config.link } : {}),
        title: "AI Translator",
        description: config.description,
        themeConfig: createLocaleThemeConfig(
            key === "root" ? "" : key,
            LOCALE_THEME_LABELS[key],
        ),
    };
}

